import { watch } from "node:fs";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "../..");
const enumPath = path.join(repoRoot, "Services/NativeMessaging/NativeMessageType.cs");
const outputPath = path.resolve(import.meta.dirname, "../src/generated/native-message-contracts.ts");
const watchMode = process.argv.includes("--watch");

await generate();

if (watchMode) {
	console.log(`[native-contracts] Watching ${path.relative(repoRoot, enumPath)}`);
	let timeout;
	const watcher = watch(enumPath, () => {
		clearTimeout(timeout);
		timeout = setTimeout(() => {
			generate().catch((error) => {
				console.error(
					error instanceof Error ? error.message : "Native contract generation failed.",
				);
			});
		}, 100);
	});

	process.on("SIGINT", () => {
		watcher.close();
		process.exit(0);
	});

	await new Promise(() => {});
}

async function generate() {
	const source = await readFile(enumPath, "utf8");
	const contracts = parseContracts(source);

	if (contracts.length === 0) {
		throw new Error(`No NativeMessageContract entries found in ${enumPath}`);
	}

	const importNames = new Set();
	for (const contract of contracts) {
		if (contract.requestType) {
			addImport(importNames, contract.requestType);
		}
		if (contract.responseType) {
			addImport(importNames, contract.responseType);
		}
	}

	const lines = [
		"/**",
		" * Auto-generated from NativeMessageType.cs.",
		" * Do not edit this file manually.",
		" */",
		"",
	];

	if (importNames.size > 0) {
		lines.push(
			`import type { ${[...importNames].sort().join(", ")} } from "./types";`,
			"",
		);
	}

	lines.push(
		"export const NativeMessageType = {",
		...contracts.map(
			(contract) => `  ${contract.messageType}: '${contract.messageType}',`,
		),
		"} as const;",
		"",
		"export type NativeMessageType = typeof NativeMessageType[keyof typeof NativeMessageType];",
		"",
		"export const NativeMessageTypesWithRequest = [",
		...contracts
			.filter((contract) => contract.requestType)
			.map((contract) => `  NativeMessageType.${contract.messageType},`),
		"] as const;",
		"",
		"export type NativeMessageTypesWithRequest = typeof NativeMessageTypesWithRequest[number];",
		"",
		"export const NativeMessageTypesWithResponse = [",
		...contracts
			.filter((contract) => contract.responseType)
			.map((contract) => `  NativeMessageType.${contract.messageType},`),
		"] as const;",
		"",
		"export type NativeMessageTypesWithResponse = typeof NativeMessageTypesWithResponse[number];",
		"export type NativeMessageTypesWithRequestAndResponse = Extract<NativeMessageTypesWithRequest, NativeMessageTypesWithResponse>;",
		"export type NativeMessageTypesWithRequestWithoutResponse = Exclude<NativeMessageTypesWithRequest, NativeMessageTypesWithResponse>;",
		"export type NativeMessageTypesWithResponseWithoutRequest = Exclude<NativeMessageTypesWithResponse, NativeMessageTypesWithRequest>;",
		"",
		"export type NativeRequestBodies = {",
		...contracts.filter((contract) => contract.requestType).map(
			(contract) =>
				`  [NativeMessageType.${contract.messageType}]: ${toTypeScriptType(contract.requestType)};`,
		),
		"};",
		"",
		"export type NativeResponseBodies = {",
		...contracts.filter((contract) => contract.responseType).map(
			(contract) =>
				`  [NativeMessageType.${contract.messageType}]: ${toTypeScriptType(contract.responseType)};`,
		),
		"};",
		"",
	);

	await writeFile(outputPath, `${lines.join("\n")}\n`, "utf8");
	console.log(`[native-contracts] Generated ${path.relative(repoRoot, outputPath)}`);
}

function parseContracts(content) {
	const pattern =
		/\[NativeMessageContract\s*\((?<args>[^\]]*)\)\s*\]\s*(?<messageType>[A-Za-z_][A-Za-z0-9_]*)/g;

	return [...content.matchAll(pattern)].map((match) => {
		const contract = parseContractArgs(match.groups.args);
		if (!contract.requestType && !contract.responseType) {
			throw new Error(
				`NativeMessageContract for '${match.groups.messageType}' must specify a request type, response type, or both.`,
			);
		}

		return {
			messageType: match.groups.messageType.trim(),
			...contract,
		};
	});
}

function parseContractArgs(args) {
	const namedRequest = args.match(
		/RequestType\s*=\s*typeof\s*\(\s*(?<type>[^)]+?)\s*\)/,
	);
	const namedResponse = args.match(
		/ResponseType\s*=\s*typeof\s*\(\s*(?<type>[^)]+?)\s*\)/,
	);

	if (namedRequest || namedResponse) {
		return {
			requestType: namedRequest?.groups.type.trim() ?? null,
			responseType: namedResponse?.groups.type.trim() ?? null,
		};
	}

	const typeMatches = [...args.matchAll(/typeof\s*\(\s*(?<type>[^)]+?)\s*\)/g)];
	return {
		requestType: typeMatches[0]?.groups.type.trim() ?? null,
		responseType: typeMatches[1]?.groups.type.trim() ?? null,
	};
}

function addImport(importNames, csharpType) {
	for (const importName of getImportNames(csharpType)) {
		importNames.add(importName);
	}
}

function getImportNames(csharpType) {
	const trimmed = csharpType.replace(/^global::/, "").trim();
	if (["object", "JsonElement", "System.Text.Json.JsonElement"].includes(trimmed)) {
		return [];
	}

	const generic = parseGenericType(trimmed);
	if (!generic) {
		return [toTypeScriptType(trimmed)];
	}

	return generic.args.flatMap(getImportNames);
}

function toTypeScriptType(csharpType) {
	const trimmed = csharpType.replace(/^global::/, "").trim();
	if (trimmed === "object") {
		return "unknown";
	}

	if (trimmed === "JsonElement" || trimmed === "System.Text.Json.JsonElement") {
		return "unknown";
	}

	const generic = parseGenericType(trimmed);
	if (!generic) {
		return trimmed.replace(/[^A-Za-z0-9_]/g, "");
	}

	if (
		generic.name === "List" ||
		generic.name === "IReadOnlyList" ||
		generic.name === "IEnumerable"
	) {
		return `${toTypeScriptType(generic.args[0])}[]`;
	}

	if (generic.name === "Dictionary" || generic.name === "IReadOnlyDictionary") {
		return `Record<${toTypeScriptType(generic.args[0])}, ${toTypeScriptType(generic.args[1])}>`;
	}

	return `${toTypeScriptType(generic.name)}<${generic.args.map(toTypeScriptType).join(", ")}>`;
}

function parseGenericType(csharpType) {
	const start = csharpType.indexOf("<");
	if (start === -1 || !csharpType.endsWith(">")) {
		return null;
	}

	return {
		name: csharpType.slice(0, start).trim(),
		args: splitGenericArgs(csharpType.slice(start + 1, -1)),
	};
}

function splitGenericArgs(args) {
	const result = [];
	let depth = 0;
	let start = 0;

	for (let index = 0; index < args.length; index++) {
		const char = args[index];
		if (char === "<") {
			depth++;
		} else if (char === ">") {
			depth--;
		} else if (char === "," && depth === 0) {
			result.push(args.slice(start, index).trim());
			start = index + 1;
		}
	}

	result.push(args.slice(start).trim());
	return result;
}
